@app.route('/stream', methods=['GET'])
def get_stream():
    """
    Main endpoint - generates fresh cookies and returns stream URL
    Usage: /stream?id=VIDEO_ID_OR_URL&format=FORMAT_ID
    """
    # Get parameters
    video_id = request.args.get('id')
    format_id = request.args.get('format', 'best')
    
    if not video_id:
        return jsonify({
            'success': False,
            'error': 'Missing id parameter',
            'usage': {
                'endpoint': '/stream?id=VIDEO_ID&format=FORMAT_ID',
                'examples': {
                    'get_360p': '/stream?id=p7ZG_xWYLzI&format=18',
                    'get_720p': '/stream?id=p7ZG_xWYLzI&format=22',
                    'get_best': '/stream?id=p7ZG_xWYLzI&format=best',
                    'get_audio_only': '/stream?id=p7ZG_xWYLzI&format=bestaudio'
                }
            }
        }), 400
    
    # Extract clean video ID
    clean_id = extract_video_id(video_id)
    if not clean_id:
        return jsonify({
            'success': False,
            'error': 'Invalid video ID or URL'
        }), 400
    
    # Generate a request ID for tracking
    request_id = str(uuid.uuid4())[:8]
    logger.info(f"[{request_id}] Processing request for video: {clean_id}, format: {format_id}")
    
    cookie_file = None
    start_time = time.time()
    
    try:
        # Get fresh cookies for this request
        cookie_file = cookie_fetcher.get_fresh_cookie_file()
        
        if not cookie_file:
            return jsonify({
                'success': False,
                'request_id': request_id,
                'error': 'Failed to generate cookies'
            }), 500
        
        # Configure yt-dlp with fresh cookies
        ydl_opts = {
            'quiet': True,
            'no_warnings': True,
            'extract_flat': False,
            'cookiefile': cookie_file,
            'format': format_id,
            'headers': {
                'Referer': 'https://www.youtube.com/',
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
            },
            'extractor_args': {
                'youtube': {
                    'skip': ['dash', 'translated_subs'],
                    'player_client': ['android', 'web']
                }
            }
        }
        
        video_url = f'https://www.youtube.com/watch?v={clean_id}'
        
        with yt_dlp.YoutubeDL(ydl_opts) as ydl:
            logger.info(f"[{request_id}] Extracting info with fresh cookies")
            info = ydl.extract_info(video_url, download=False)
            
            # Find the stream URL
            stream_url = None
            format_info = None
            
            # Try to get URL from formats list
            if 'formats' in info:
                for f in info['formats']:
                    if f.get('format_id') == format_id and f.get('url'):
                        stream_url = f['url']
                        format_info = {
                            'format_id': f.get('format_id'),
                            'ext': f.get('ext'),
                            'resolution': f.get('resolution') or f.get('format_note') or 'N/A',
                            'filesize': f.get('filesize'),
                            'vcodec': f.get('vcodec', 'none'),
                            'acodec': f.get('acodec', 'none')
                        }
                        break
            
            # If not found in formats, try the main URL
            if not stream_url and 'url' in info:
                stream_url = info['url']
                format_info = {
                    'format_id': format_id,
                    'ext': info.get('ext', 'unknown'),
                    'resolution': 'best'
                }
            
            if stream_url:
                elapsed_time = time.time() - start_time
                logger.info(f"[{request_id}] Success - Time: {elapsed_time:.2f}s")
                
                return jsonify({
                    'success': True,
                    'stream_url': stream_url,
                    'video_id': clean_id,
                    'title': info.get('title'),
                    'duration': info.get('duration'),
                    'uploader': info.get('uploader'),
                    'format': format_info,
                    'format_requested': format_id,
                    'view_count': info.get('view_count'),
                    'like_count': info.get('like_count'),
                    'request_id': request_id,
                    'processing_time': round(elapsed_time, 2),
                    'cookies_used': cookie_file
                })
            else:
                # Get available formats
                available_formats = []
                if 'formats' in info:
                    for f in info['formats'][:10]:  # Limit to 10 formats
                        available_formats.append({
                            'format_id': f.get('format_id'),
                            'ext': f.get('ext'),
                            'resolution': f.get('resolution') or f.get('format_note') or 'N/A',
                            'has_video': f.get('vcodec') != 'none',
                            'has_audio': f.get('acodec') != 'none'
                        })
                
                elapsed_time = time.time() - start_time
                return jsonify({
                    'success': False,
                    'error': f'Format {format_id} not available',
                    'available_formats': available_formats,
                    'title': info.get('title'),
                    'request_id': request_id,
                    'processing_time': round(elapsed_time, 2)
                }), 404
    
    except Exception as e:
        error_msg = str(e)
        error_msg = re.sub(r'\x1b\[[0-9;]*m', '', error_msg)
        logger.error(f"[{request_id}] Error: {error_msg}")
        
        elapsed_time = time.time() - start_time
        return jsonify({
            'success': False,
            'error': error_msg,
            'request_id': request_id,
            'processing_time': round(elapsed_time, 2)
        }), 500
    
    finally:
        # Clean up old cookie files periodically (not the one we just used)
        if request_id and int(request_id[:2], 16) % 10 == 0:  # Random cleanup
            cleanup_old_cookie_files()

@app.route('/info', methods=['GET'])
def get_info():
    """
    Get video information without stream URL
    Usage: /info?id=VIDEO_ID_OR_URL
    """
    video_id = request.args.get('id')
    
    if not video_id:
        return jsonify({
            'success': False,
            'error': 'Missing id parameter'
        }), 400
    
    clean_id = extract_video_id(video_id)
    if not clean_id:
        return jsonify({
            'success': False,
            'error': 'Invalid video ID or URL'
        }), 400
    
    request_id = str(uuid.uuid4())[:8]
    cookie_file = None
    start_time = time.time()
    
    try:
        # Get fresh cookies for this request
        cookie_file = cookie_fetcher.get_fresh_cookie_file()
        
        if not cookie_file:
            return jsonify({
                'success': False,
                'request_id': request_id,
                'error': 'Failed to generate cookies'
            }), 500
        
        # Configure yt-dlp
        ydl_opts = {
            'quiet': True,
            'no_warnings': True,
            'extract_flat': False,
            'cookiefile': cookie_file,
            'headers': {
                'Referer': 'https://www.youtube.com/',
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
            }
        }
        
        video_url = f'https://www.youtube.com/watch?v={clean_id}'
        
        with yt_dlp.YoutubeDL(ydl_opts) as ydl:
            info = ydl.extract_info(video_url, download=False)
            
            # Extract format information
            formats = []
            if 'formats' in info:
                for f in info['formats']:
                    formats.append({
                        'format_id': f.get('format_id'),
                        'ext': f.get('ext'),
                        'resolution': f.get('resolution') or f.get('format_note') or 'N/A',
                        'filesize_mb': round(f.get('filesize', 0) / (1024 * 1024), 2) if f.get('filesize') else None,
                        'has_video': f.get('vcodec') != 'none',
                        'has_audio': f.get('acodec') != 'none',
                        'fps': f.get('fps'),
                        'vcodec': f.get('vcodec'),
                        'acodec': f.get('acodec')
                    })
            
            elapsed_time = time.time() - start_time
            return jsonify({
                'success': True,
                'request_id': request_id,
                'video_id': clean_id,
                'title': info.get('title'),
                'duration': info.get('duration'),
                'uploader': info.get('uploader'),
                'view_count': info.get('view_count'),
                'like_count': info.get('like_count'),
                'description': info.get('description')[:500] + '...' if info.get('description') and len(info.get('description')) > 500 else info.get('description'),
                'thumbnail': info.get('thumbnail'),
                'upload_date': info.get('upload_date'),
                'formats': formats,
                'format_count': len(formats),
                'processing_time': round(elapsed_time, 2)
            })
    
    except Exception as e:
        error_msg = re.sub(r'\x1b\[[0-9;]*m', '', str(e))
        elapsed_time = time.time() - start_time
        return jsonify({
            'success': False,
            'request_id': request_id,
            'error': error_msg,
            'processing_time': round(elapsed_time, 2)
        }), 500
    
    finally:
        # Clean up
        if cookie_file and os.path.exists(cookie_file):
            try:
                os.unlink(cookie_file)
                logger.debug(f"[{request_id}] Cleaned up cookie file: {cookie_file}")
            except:
                pass

