class YouTubeCookieFetcher:
    """Exact same cookie fetcher from the working script"""
    
    def __init__(self, cookie_file='cookies.txt'):
        """
        Initialize the cookie fetcher
        """
        self.cookie_file = cookie_file
        self.session = requests.Session()
        self.last_fetch_time = None
        self.current_cookies = None
        self.lock = threading.Lock()
        
    def get_headers(self) -> Dict:
        """Get realistic browser headers"""
        return {
            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,*/*;q=0.8',
            'Accept-Language': 'en-US,en;q=0.5',
            'Accept-Encoding': 'gzip, deflate, br',
            'DNT': '1',
            'Connection': 'keep-alive',
            'Upgrade-Insecure-Requests': '1',
            'Sec-Fetch-Dest': 'document',
            'Sec-Fetch-Mode': 'navigate',
            'Sec-Fetch-Site': 'none',
            'Sec-Fetch-User': '?1',
            'Cache-Control': 'max-age=0',
        }
    
    def save_cookies_netscape_format(self, cookies_dict: Dict, domain: str = ".youtube.com") -> Optional[str]:
        """
        Save cookies in Netscape format (cookies.txt format)
        Returns path to cookie file
        """
        try:
            # Calculate expiration (1 year from now for persistent cookies)
            expiration = int((datetime.now() + timedelta(days=365)).timestamp())
            
            # Create a unique filename for this cookie batch
            timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
            unique_id = str(uuid.uuid4())[:8]
            cookie_file = f'cookies_{timestamp}_{unique_id}.txt'
            
            with open(cookie_file, 'w') as f:
                # Write header
                f.write("# Netscape HTTP Cookie File\n")
                f.write("# https://curl.se/docs/http-cookies.html\n")
                f.write("# This file was generated by YouTube Cookie Fetcher\n")
                f.write(f"# Generated: {datetime.now().isoformat()}\n\n")
                
                # Write each cookie
                for name, value in cookies_dict.items():
                    # Determine if secure (HTTPS only)
                    secure = "TRUE" if name.startswith('__Secure') else "FALSE"
                    
                    # Write cookie line
                    f.write(f"{domain}\tTRUE\t/\t{secure}\t{expiration}\t{name}\t{value}\n")
                
                f.write("\n")
            
            logger.info(f"✅ Cookies saved in Netscape format to {cookie_file}")
            return cookie_file
            
        except Exception as e:
            logger.error(f"Failed to save cookies: {e}")
            return None
    
    def fetch_cookies(self) -> Optional[Dict]:
        """
        Fetch fresh cookies from YouTube
        """
        try:
            logger.info("🌐 Fetching fresh cookies from YouTube...")
            
            # Clear previous cookies
            self.session.cookies.clear()
            
            # Make request to YouTube
            response = self.session.get(
                'https://www.youtube.com',
                headers=self.get_headers(),
                timeout=15,
                allow_redirects=True
            )
            
            if response.status_code != 200:
                logger.error(f"Failed to fetch YouTube. Status: {response.status_code}")
                return None
            
            # Get cookies from session
            cookies_dict = self.session.cookies.get_dict()
            
            if not cookies_dict:
                logger.warning("No cookies received")
                return None
            
            # Create cookie data
            cookie_data = {
                'timestamp': datetime.now().isoformat(),
                'timestamp_epoch': int(time.time()),
                'cookies': cookies_dict,
                'cookie_count': len(cookies_dict),
                'status_code': response.status_code
            }
            
            logger.info(f"✅ Successfully fetched {len(cookies_dict)} cookies")
            return cookie_data
            
        except requests.exceptions.Timeout:
            logger.error("Request timeout")
        except requests.exceptions.ConnectionError:
            logger.error("Connection error")
        except requests.exceptions.RequestException as e:
            logger.error(f"Request error: {e}")
        except Exception as e:
            logger.error(f"Unexpected error: {e}")
        
        return None
    
    def verify_cookies(self, cookie_data: Dict) -> bool:
        """Verify if cookies are valid"""
        try:
            test_session = requests.Session()
            
            # Set cookies
            for name, value in cookie_data['cookies'].items():
                test_session.cookies.set(name, value)
            
            # Test request
            response = test_session.get(
                'https://www.youtube.com',
                headers=self.get_headers(),
                timeout=10
            )
            
            if response.status_code == 200:
                logger.info("✅ Cookies verified and working")
                return True
            else:
                logger.warning(f"⚠️ Cookie verification failed. Status: {response.status_code}")
                return False
                
        except Exception as e:
            logger.error(f"Verification error: {e}")
            return False
    
    def get_fresh_cookie_file(self) -> Optional[str]:
        """
        Get a fresh cookie file for immediate use
        This is the main method to call for each request
        """
        with self.lock:
            # Fetch fresh cookies
            cookie_data = self.fetch_cookies()
            
            if not cookie_data:
                return None
            
            # Verify cookies
            if not self.verify_cookies(cookie_data):
                logger.warning("Cookie verification failed, but will try to use them anyway")
            
            # Save to file and return path
            cookie_file = self.save_cookies_netscape_format(cookie_data['cookies'])
            
            # Store current cookies
            self.current_cookies = cookie_data
            self.last_fetch_time = datetime.now()
            
            return cookie_file

def extract_video_id(url_or_id):
    """Extract YouTube video ID from URL or return the ID if already clean"""
    if re.match(r'^[a-zA-Z0-9_-]{11}$', url_or_id):
        return url_or_id
    
    patterns = [
        r'(?:youtube\.com\/watch\?v=)([^&]+)',
        r'(?:youtu\.be\/)([^?]+)',
        r'(?:youtube\.com\/embed\/)([^?]+)',
        r'(?:youtube\.com\/v\/)([^?]+)'
    ]
    
    for pattern in patterns:
        match = re.search(pattern, url_or_id)
        if match:
            return match.group(1)
    
    return None

def cleanup_old_cookie_files(max_age_minutes=5):
    """
    Clean up old cookie files
    """
    try:
        current_time = time.time()
        for filename in os.listdir('.'):
            if filename.startswith('cookies_') and filename.endswith('.txt'):
                file_path = os.path.join('.', filename)
                file_age = current_time - os.path.getmtime(file_path)
                
                # Delete files older than max_age_minutes
                if file_age > (max_age_minutes * 60):
                    try:
                        os.unlink(file_path)
                        logger.debug(f"Cleaned up old cookie file: {filename}")
                    except Exception as e:
                        logger.warning(f"Failed to delete {filename}: {e}")
    except Exception as e:
        logger.error(f"Cleanup error: {e}")

